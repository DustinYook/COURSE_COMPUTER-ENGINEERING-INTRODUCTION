# 5) BMI 계산기
weight = float(input('몸무게를 kg 단위로 입력하십시오 : '))
height = float(input('키를 m 단위로 입력하십시오 : '))
bmi = weight / height ** 2  # 주의) 연산자 '**'의 우선순위는 '/' 보다 높음!
# 참고) 다른 표현방법 : bmi = weight / pow(height, 2)
print('BMI : {:#.2f}'.format(bmi))


# 6-1) 잔돈 계산하기 -> 나의 방법
money = int(input('변환할 금액을 1000원 단위로 입력해주십시오 : '))  # 788,000원
print('입력하신 금액은 {:#d}원 입니다.'.format(money))
temp = money  # 원본 데이터를 수정하지 않게 하는 처리
cnt1 = temp // 50000  # 5만원권의 장수를 계산하는 처리
temp -= (50000 * cnt1)  # 5만원권 뺀 나머지 금액
cnt2 = temp // 10000  # 1만원권의 장수를 계산하는 처리
temp -= (10000 * cnt2)  # 1만원권 뺀 나머지 금액
cnt3 = temp // 5000  # 5천원권의 장수를 계산하는 처리
temp -= (5000 * cnt3)  # 5천원권 뺀 나머지 금액
cnt4 = temp // 1000  # 1천원권의 장수를 계산하는 처리
temp -= (1000 * cnt4)  # 1천원권 뺀 나머지 금액
print('5만원권:{}장, 1만원권:{}장, 5천원권:{}장, 1천원권:{}장\n'.format(cnt1, cnt2, cnt3, cnt4))

# 6-2) 잔돈 계산하기 -> 교수님의 방법
money = int(input('변환할 금액을 1000원 단위로 입력해주십시오 : '))  # 788,000원
print('입력하신 금액은 {:#d}원 입니다.'.format(money))
cnt1 = money // 50000  # 5만원권의 장수를 계산하는 처리
rem1 = money % 50000  # 5만원권 뺀 나머지 금액
cnt2 = rem1 // 10000  # 1만원권의 장수를 계산하는 처리
rem2 = rem1 % 10000  # 1만원권 뺀 나머지 금액
cnt3 = rem2 // 5000  # 5천원권의 장수를 계산하는 처리
rem3 = rem2 % 5000  # 5천원권 뺀 나머지 금액
cnt4 = rem3 // 1000  # 1천원권의 장수를 계산하는 처리
print('5만원권:{}장, 1만원권:{}장, 5천원권:{}장, 1천원권:{}장\n'.format(cnt1, cnt2, cnt3, cnt4))

