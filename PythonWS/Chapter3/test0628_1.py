# 프로그램 목적 : 연산자를 이용한 프로그래밍 실습

# 1-1) 연산자 다루기 1
p = int(input('분자를 입력하시오 : '))
q = int(input('분모를 입력하시오 : '))
print('나눗셈의 몫은 \'{:#d}\'이고, 나눗셈의 나머지는 \'{:#d}\'입니다.\n'.format(p // q, p % q))

# 1-2) 연산자 다루기 2 -> 연산자 혼동에 주의!
a = int(input('분자를 입력하시오 : '))
b = int(input('분모를 입력하시오 : '))
print('{:#d} / {:#d} == {:#.2f}'.format(a, b, a / b))  # '/' 연산자는 실수 나눗셈 연산자
print('{:#d} // {:#d} == {:#d}'.format(a, b, a // b))  # '//' 연산자는 정수 나눗셈 연산자 (몫)

# 2) 초단위의 시간을 입력 받아서 시, 분, 초 출력
time = int(input('변환하고 싶은 시간을 초 단위로 입력해주십시오 : '))
print('입력하신 시간은 {:#d}초 입니다.'.format(time))
second = (time % 60)  # 초를 구하는 처리        -> 핵심코드!
minute = (time // 60) % 60  # 분을 구하는 처리  -> 핵심코드!
hour = (time // 60) // 60  # 시간을 구하는 처리 -> 핵심코드!
print('변환된 시간은 \'{:#d}시 {:#d}분 {:#d}초\' 입니다.\n'.format(hour, minute, second))

# 3-1) 화씨온도를 섭씨온도로 변환하는 프로그램
